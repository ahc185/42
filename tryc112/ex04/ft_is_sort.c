int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int i;
	int j;
	int k;

	i = 0;
	k = 1;
	j = 1;
	while (i < length - 1)
	{
		if (f(tab[i], tab[i + 1]) < 0)
			k = 0;
		if (f(tab[i], tab[i + 1]) > 0)
			j = 0;
		if (j == 0 && k == 0)
			return (0);
		i++;
	}
	return (1);
}
