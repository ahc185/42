#include <string.h>

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	int			i;
	char		*d;
	const char	*s;

	i = 0;
	d = dest;
	s = src;
	while (n)
	{
		d[i] = s[i];
		i++;
		n--;
	}
	return (dest);
}
